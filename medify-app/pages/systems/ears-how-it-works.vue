            <h2>How Hearing Works</h2>
            
            <h3>Step 1: Sound Collection</h3>
            <ol>
              <li>Sound waves travel through air</li>
              <li>Outer ear collects and funnels sound</li>
              <li>Sound waves travel down ear canal</li>
              <li>Sound waves hit the eardrum</li>
            </ol>

            <h3>Step 2: Vibration Amplification</h3>
            <ol>
              <li>Eardrum vibrates when hit by sound waves</li>
              <li>Vibrations pass to three tiny bones</li>
              <li>Bones amplify the vibrations</li>
              <li>Stirrup bone pushes against inner ear</li>
            </ol>

            <h3>Step 3: Sound Conversion</h3>
            <ol>
              <li>Vibrations enter the cochlea</li>
              <li>Cochlea is filled with fluid</li>
              <li>Fluid moves and bends tiny hair cells</li>
              <li>Hair cells convert movement into electrical signals</li>
            </ol>

            <h3>Step 4: Signal Transmission</h3>
            <ol>
              <li>Electrical signals travel through auditory nerve</li>
              <li>Signals reach the brain's hearing center</li>
              <li>Brain processes and interprets the signals</li>
              <li>You "hear" the sound</li>
            </ol>

            <h2>Hair Cells - The Key Players</h2>
            <p>Inside the cochlea are thousands of tiny hair cells:</p>
            
            <ul>
              <li><strong>Outer hair cells</strong>: Amplify sound vibrations</li>
              <li><strong>Inner hair cells</strong>: Convert vibrations to electrical signals</li>
              <li><strong>Frequency detection</strong>: Different cells detect different pitches</li>
              <li><strong>Damage</strong>: Once damaged, hair cells don't grow back</li>
            </ul>

            <h2>How We Hear Different Frequencies</h2>
            <p>Your cochlea is organized like a piano keyboard:</p>
            
            <ul>
              <li><strong>High frequencies</strong>: Detected near the entrance of cochlea</li>
              <li><strong>Low frequencies</strong>: Detected deeper in the cochlea</li>
              <li><strong>Middle frequencies</strong>: Detected in the middle</li>
              <li><strong>Frequency range</strong>: About 20 Hz to 20,000 Hz</li>
            </ul>

            <h2>Balance System</h2>
            <p>Your inner ear also helps you maintain balance:</p>
            
            <h3>Semicircular Canals</h3>
            <ul>
              <li>Three fluid-filled canals</li>
              <li>Detect head rotation in three directions</li>
              <li>Send signals about head movement to brain</li>
            </ul>

            <h3>Vestibule</h3>
            <ul>
              <li>Detects linear movement (up/down, forward/back)</li>
              <li>Contains otoliths (tiny crystals)</li>
              <li>Helps you know which way is up</li>
            </ul>

            <h2>Protective Mechanisms</h2>
            <p>Your ears have built-in protection:</p>
            
            <ul>
              <li><strong>Earwax</strong>: Traps dirt and protects ear canal</li>
              <li><strong>Ear canal shape</strong>: Curved to prevent objects from reaching eardrum</li>
              <li><strong>Reflex response</strong>: Muscles protect from loud sounds</li>
              <li><strong>Eustachian tube</strong>: Equalizes pressure changes</li>
            </ul>

            <h2>Sound Localization</h2>
            <p>Having two ears helps you locate sounds:</p>
            
            <ul>
              <li><strong>Time difference</strong>: Sound reaches one ear before the other</li>
              <li><strong>Volume difference</strong>: Sound is louder in one ear</li>
              <li><strong>Brain processing</strong>: Combines information from both ears</li>
              <li><strong>3D hearing</strong>: Allows you to pinpoint sound location</li>
            </ul>

            <h2>Hearing Range</h2>
            <p>Human hearing can detect:</p>
            
            <ul>
              <li><strong>Frequency range</strong>: 20 Hz to 20,000 Hz</li>
              <li><strong>Volume range</strong>: From whisper to jet engine</li>
              <li><strong>Speech range</strong>: 250 Hz to 4,000 Hz</li>
              <li><strong>Music range</strong>: 20 Hz to 20,000 Hz</li>
            </ul>

            <h2>Why It's Critical</h2>
