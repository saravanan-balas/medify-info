            <h2>Three Main Parts of Your Ear</h2>
            
            <h3>Outer Ear</h3>
            <ul>
              <li><strong>Pinna (Auricle)</strong>: The visible part of your ear</li>
              <li><strong>Ear canal</strong>: Tube that leads to eardrum</li>
              <li><strong>Earwax</strong>: Protects and cleans the ear canal</li>
              <li>Collects and funnels sound waves</li>
            </ul>

            <h3>Middle Ear</h3>
            <ul>
              <li><strong>Eardrum (Tympanic membrane)</strong>: Thin membrane that vibrates</li>
              <li><strong>Three tiny bones (Ossicles)</strong>: Hammer, anvil, and stirrup</li>
              <li><strong>Eustachian tube</strong>: Connects to throat, equalizes pressure</li>
              <li>Amplifies sound vibrations</li>
            </ul>

            <h3>Inner Ear</h3>
            <ul>
              <li><strong>Cochlea</strong>: Snail-shaped structure for hearing</li>
              <li><strong>Vestibular system</strong>: Three semicircular canals for balance</li>
              <li><strong>Auditory nerve</strong>: Carries signals to brain</li>
