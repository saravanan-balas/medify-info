            <h2>Your Kidneys - Amazing Filters</h2>
            <p>Your kidneys are like sophisticated water treatment plants that filter your blood 24/7, removing waste products and maintaining the perfect balance of fluids and chemicals in your body.</p>
            
            <h2>Kidney Structure</h2>
            
            <h3>Location and Size</h3>
            <ul>
              <li>Located on either side of your spine, below your ribs</li>
              <li>Each kidney is about the size of your fist</li>
              <li>Bean-shaped organs</li>
              <li>Protected by your lower ribs</li>
            </ul>

            <h3>Main Parts</h3>
            <ul>
              <li><strong>Renal cortex</strong>: Outer layer where filtering happens</li>
              <li><strong>Renal medulla</strong>: Inner layer with collecting ducts</li>
              <li><strong>Renal pelvis</strong>: Collects urine before it leaves kidney</li>
              <li><strong>Ureter</strong>: Tube that carries urine to bladder</li>
            </ul>

            <h2>How Filtration Works</h2>
            
            <h3>Step 1: Blood Enters Kidneys</h3>
            <ol>
              <li>Blood flows into kidneys through renal arteries</li>
              <li>About 1.2 liters of blood per minute</li>
              <li>Blood contains waste products and excess water</li>
            </ol>

            <h3>Step 2: Filtration in Nephrons</h3>
            <ol>
              <li>Each kidney contains about 1 million nephrons</li>
              <li>Nephrons are tiny filtering units</li>
              <li>Blood pressure forces fluid through filters</li>
              <li>Waste products and water are filtered out</li>
              <li>Important substances are kept in blood</li>
            </ol>

            <h3>Step 3: Reabsorption</h3>
            <ol>
              <li>Useful substances are reabsorbed back into blood</li>
              <li>Water, glucose, and minerals are reclaimed</li>
              <li>About 99% of filtered fluid is reabsorbed</li>
              <li>Only waste products remain</li>
            </ol>

            <h3>Step 4: Urine Formation</h3>
            <ol>
              <li>Remaining fluid becomes urine</li>
              <li>Urine contains waste products and excess water</li>
              <li>Urine flows to bladder through ureters</li>
            </ol>

            <h2>What Kidneys Filter Out</h2>
            
            <h3>Waste Products</h3>
            <ul>
              <li><strong>Urea</strong>: From protein breakdown</li>
              <li><strong>Creatinine</strong>: From muscle activity</li>
              <li><strong>Uric acid</strong>: From cell breakdown</li>
              <li><strong>Drugs and toxins</strong>: Medications and harmful substances</li>
            </ul>

            <h3>Excess Substances</h3>
            <ul>
              <li><strong>Water</strong>: When you drink too much</li>
              <li><strong>Salt</strong>: Excess sodium</li>
              <li><strong>Potassium</strong>: Excess potassium</li>
              <li><strong>Other minerals</strong>: Excess minerals</li>
            </ul>

            <h2>What Kidneys Keep</h2>
            <p>Important substances are reabsorbed back into blood:</p>
            
            <ul>
              <li><strong>Glucose</strong>: Sugar needed for energy</li>
              <li><strong>Water</strong>: When body needs it</li>
              <li><strong>Proteins</strong>: Needed for body functions</li>
              <li><strong>Essential minerals</strong>: Sodium, potassium, calcium</li>
            </ul>

            <h2>Other Important Functions</h2>
            
            <h3>Blood Pressure Control</h3>
            <ul>
              <li>Kidneys regulate blood volume</li>
              <li>Release hormones that control blood pressure</li>
              <li>Remove excess salt and water</li>
            </ul>

            <h3>Red Blood Cell Production</h3>
            <ul>
              <li>Kidneys produce erythropoietin hormone</li>
              <li>Stimulates bone marrow to make red blood cells</li>
              <li>Red blood cells carry oxygen throughout body</li>
            </ul>

            <h3>Bone Health</h3>
            <ul>
              <li>Activate vitamin D for calcium absorption</li>
              <li>Help maintain strong bones</li>
              <li>Prevent bone diseases</li>
            </ul>

            <h3>Acid-Base Balance</h3>
            <ul>
              <li>Remove excess acid from blood</li>
              <li>Maintain proper pH levels</li>
              <li>Keep blood slightly alkaline</li>
            </ul>

            <h2>Urinary System</h2>
            
            <h3>Ureters</h3>
            <ul>
              <li>Tubes that carry urine from kidneys to bladder</li>
              <li>About 10-12 inches long</li>
              <li>Have muscle contractions to move urine</li>
            </ul>

            <h3>Bladder</h3>
            <ul>
              <li>Muscular sac that stores urine</li>
              <li>Can hold about 400-600ml of urine</li>
              <li>Expands as it fills</li>
              <li>Sends signals when it needs to empty</li>
            </ul>

            <h3>Urethra</h3>
            <ul>
              <li>Tube that carries urine out of body</li>
              <li>Shorter in women, longer in men</li>
              <li>Has sphincter muscles to control flow</li>
            </ul>

            <h2>Urination Process</h2>
            <ol>
              <li>Bladder fills with urine</li>
              <li>Nerves send signals to brain</li>
              <li>Brain decides when to urinate</li>
              <li>Sphincter muscles relax</li>
              <li>Bladder muscle contracts</li>
              <li>Urine flows out through urethra</li>
            </ol>

            <h2>Why It's Critical</h2>
            <p>Without your kidneys and urinary system, waste products would build up in your blood, leading to serious illness and death. Your kidneys are essential for maintaining the delicate balance your body needs to function properly!</p>
