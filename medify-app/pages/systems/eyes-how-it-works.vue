            <h2>Your Eyes - Amazing Cameras</h2>
            <p>Your eyes are like sophisticated cameras that capture light and convert it into electrical signals your brain can understand. They're one of the most complex organs in your body!</p>
            
            <h2>Main Parts of Your Eye</h2>
            
            <h3>Cornea</h3>
            <ul>
              <li>Clear front surface that covers the eye</li>
              <li>Acts like a camera lens</li>
              <li>Focuses light as it enters the eye</li>
              <li>Has no blood vessels (gets oxygen from air)</li>
            </ul>

            <h3>Iris</h3>
            <ul>
              <li>Colored part of your eye</li>
              <li>Controls how much light enters</li>
              <li>Expands in dim light, contracts in bright light</li>
              <li>Like a camera's aperture</li>
            </ul>

            <h3>Pupil</h3>
            <ul>
              <li>Black center of the iris</li>
              <li>Opening that lets light through</li>
              <li>Gets bigger in dim light, smaller in bright light</li>
              <li>Like a camera's shutter</li>
            </ul>

            <h3>Lens</h3>
            <ul>
              <li>Clear structure behind the iris</li>
              <li>Focuses light onto the retina</li>
              <li>Changes shape to focus on near or far objects</li>
              <li>Becomes less flexible with age</li>
            </ul>

            <h3>Retina</h3>
            <ul>
              <li>Light-sensitive layer at the back of the eye</li>
              <li>Contains millions of light receptors</li>
              <li>Like the film in a camera</li>
              <li>Converts light into electrical signals</li>
            </ul>

            <h3>Optic Nerve</h3>
            <ul>
              <li>Carries visual information to the brain</li>
              <li>Like a cable connecting camera to computer</li>
              <li>Contains over 1 million nerve fibers</li>
            </ul>

            <h2>How Vision Works</h2>
            
            <h3>Step 1: Light Enters</h3>
            <ol>
              <li>Light reflects off objects around you</li>
              <li>Light enters through the cornea</li>
              <li>Cornea bends (refracts) the light</li>
              <li>Light passes through the pupil</li>
            </ol>

            <h3>Step 2: Light Gets Focused</h3>
            <ol>
              <li>Light passes through the lens</li>
              <li>Lens focuses light onto the retina</li>
              <li>Image is formed upside down on retina</li>
              <li>Like a camera focusing on film</li>
            </ol>

            <h3>Step 3: Light Gets Converted</h3>
            <ol>
              <li>Retina contains two types of light receptors</li>
              <li><strong>Rods</strong>: Detect light and dark (work in dim light)</li>
              <li><strong>Cones</strong>: Detect colors (work in bright light)</li>
              <li>Receptors convert light into electrical signals</li>
            </ol>

            <h3>Step 4: Signals Go to Brain</h3>
            <ol>
              <li>Electrical signals travel through optic nerve</li>
              <li>Signals reach the visual cortex in brain</li>
              <li>Brain processes and interprets the signals</li>
              <li>Brain flips the image right-side up</li>
              <li>You "see" the image</li>
            </ol>

            <h2>Color Vision</h2>
            <p>Your eyes can see millions of colors using three types of cones:</p>
            
            <ul>
              <li><strong>Red cones</strong>: Detect red light</li>
              <li><strong>Green cones</strong>: Detect green light</li>
              <li><strong>Blue cones</strong>: Detect blue light</li>
              <li><strong>Combined signals</strong>: Create all other colors</li>
            </ul>

            <h2>Depth Perception</h2>
            <p>Having two eyes helps you see depth:</p>
            
            <ul>
              <li><strong>Binocular vision</strong>: Each eye sees slightly different images</li>
              <li><strong>Brain combines</strong>: The two images into one 3D picture</li>
              <li><strong>Depth cues</strong>: Help judge distances</li>
            </ul>

            <h2>Eye Movements</h2>
            <p>Six muscles control each eye's movement:</p>
            
            <ul>
              <li><strong>Up, down, left, right</strong>: Basic movements</li>
              <li><strong>Tracking</strong>: Following moving objects</li>
              <li><strong>Saccades</strong>: Quick jumps between points</li>
              <li><strong>Convergence</strong>: Eyes turn inward for near objects</li>
            </ul>

            <h2>Protective Features</h2>
            <p>Your eyes have built-in protection:</p>
            
            <ul>
              <li><strong>Eyelids</strong>: Blink to moisten and protect</li>
              <li><strong>Eyelashes</strong>: Filter out dust and particles</li>
              <li><strong>Tears</strong>: Keep eyes moist and clean</li>
              <li><strong>Eyebrows</strong>: Prevent sweat from dripping into eyes</li>
            </ul>

            <h2>Why It's Critical</h2>
            <p>Your eyes provide 80% of the information your brain receives. They enable you to navigate your environment safely, recognize faces and emotions, and enjoy the beauty of the world around you!</p>
