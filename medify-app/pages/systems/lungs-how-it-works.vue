    <section class="py-16">
      <div class="container-custom">
        <div class="max-w-4xl mx-auto">
          <div class="prose prose-lg max-w-none">
            <h2>The Respiratory System - Your Body's Air Exchange</h2>
            <p>Your respiratory system is like a sophisticated air processing plant that takes in fresh air, extracts what your body needs, and gets rid of waste gases.</p>
            
            <h2>Main Parts of Your Respiratory System</h2>
            
            <h3>Nose and Mouth</h3>
            <ul>
              <li><strong>Entry points</strong>: Where air enters your body</li>
              <li><strong>Nose filters</strong>: Tiny hairs trap dust and particles</li>
              <li><strong>Mucus</strong>: Moistens air and catches germs</li>
              <li><strong>Warming</strong>: Cold air is warmed before reaching lungs</li>
            </ul>

            <h3>Trachea (Windpipe)</h3>
            <ul>
              <li>Tube that carries air from throat to lungs</li>
              <li>Protected by cartilage rings</li>
              <li>Lined with tiny hairs that sweep mucus upward</li>
            </ul>

            <h3>Bronchi and Bronchioles</h3>
            <ul>
              <li><strong>Bronchi</strong>: Main branches leading to each lung</li>
              <li><strong>Bronchioles</strong>: Smaller branches that spread throughout lungs</li>
              <li>Like a tree with many branches</li>
            </ul>

            <h3>Lungs</h3>
            <ul>
              <li><strong>Right lung</strong>: Has 3 lobes</li>
              <li><strong>Left lung</strong>: Has 2 lobes (makes room for heart)</li>
              <li>Spongy organs that expand and contract</li>
            </ul>

            <h3>Alveoli (Air Sacs)</h3>
            <ul>
              <li>Tiny air sacs at the end of bronchioles</li>
              <li>Where gas exchange actually happens</li>
              <li>Millions of them - like tiny balloons</li>
              <li>Surrounded by blood vessels</li>
            </ul>

            <h2>How Breathing Works</h2>
            
            <h3>Inhalation (Breathing In)</h3>
            <ol>
              <li><strong>Diaphragm contracts</strong>: Moves down, creating more space</li>
              <li><strong>Rib muscles contract</strong>: Ribs move up and out</li>
              <li><strong>Chest expands</strong>: Creates negative pressure</li>
              <li><strong>Air rushes in</strong>: Like a vacuum sucking air in</li>
            </ol>

            <h3>Exhalation (Breathing Out)</h3>
            <ol>
              <li><strong>Diaphragm relaxes</strong>: Moves back up</li>
              <li><strong>Rib muscles relax</strong>: Ribs move down and in</li>
              <li><strong>Chest contracts</strong>: Pushes air out</li>
              <li><strong>Air flows out</strong>: Like squeezing a balloon</li>
            </ol>

            <h2>Gas Exchange - The Magic Happens Here</h2>
            <p>In the alveoli, oxygen and carbon dioxide are exchanged:</p>
            
            <h3>Oxygen Exchange</h3>
            <ol>
              <li>Oxygen from air enters alveoli</li>
              <li>Oxygen crosses thin walls into blood vessels</li>
              <li>Red blood cells pick up oxygen</li>
              <li>Oxygen-rich blood travels to heart</li>
              <li>Heart pumps oxygen to entire body</li>
            </ol>

            <h3>Carbon Dioxide Exchange</h3>
            <ol>
              <li>Blood brings carbon dioxide to lungs</li>
              <li>Carbon dioxide crosses from blood to alveoli</li>
              <li>Carbon dioxide mixes with air in alveoli</li>
              <li>You breathe out the carbon dioxide</li>
            </ol>

            <h2>Breathing Rate</h2>
            <p>Your breathing rate changes based on your body's needs:</p>
            
            <ul>
              <li><strong>Resting</strong>: 12-20 breaths per minute</li>
              <li><strong>Exercise</strong>: Can increase to 40+ breaths per minute</li>
              <li><strong>Sleep</strong>: Slows down</li>
              <li><strong>Stress</strong>: Can increase due to anxiety</li>
            </ul>

            <h2>Lung Capacity</h2>
            <p>Your lungs can hold different amounts of air:</p>
            
            <ul>
              <li><strong>Tidal volume</strong>: Normal breath (about 500ml)</li>
              <li><strong>Vital capacity</strong>: Maximum breath out after deep breath in</li>
              <li><strong>Total capacity</strong>: All air lungs can hold (about 6 liters)</li>
            </ul>

            <h2>Protective Mechanisms</h2>
            <p>Your respiratory system has built-in protection:</p>
            
            <ul>
              <li><strong>Coughing</strong>: Clears irritants and mucus</li>
              <li><strong>Sneezing</strong>: Removes particles from nose</li>
              <li><strong>Cilia</strong>: Tiny hairs that sweep mucus upward</li>
              <li><strong>Mucus</strong>: Traps dust, germs, and particles</li>
              <li><strong>Immune cells</strong>: Fight infections in lungs</li>
            </ul>

            <h2>Detailed Anatomy</h2>
            
            <h3>Lung Structure</h3>
            <ul>
              <li><strong>Right Lung</strong>: Three lobes (superior, middle, inferior); larger than left lung; separated by horizontal and oblique fissures</li>
              <li><strong>Left Lung</strong>: Two lobes (superior, inferior); smaller due to heart space; separated by oblique fissure; has cardiac notch</li>
              <li><strong>Lung Apex</strong>: Top of lung, extends above clavicle; protected by rib cage</li>
              <li><strong>Lung Base</strong>: Bottom of lung, sits on diaphragm; concave shape matches diaphragm</li>
            </ul>

            <h3>Airway Branching (Bronchial Tree)</h3>
            <ul>
              <li><strong>Trachea</strong>: 10-12 cm long, 2-2.5 cm diameter; C-shaped cartilage rings (16-20 rings); divides into two main bronchi at carina</li>
              <li><strong>Primary Bronchi</strong>: Right and left main bronchi; right is wider, shorter, more vertical (more likely for foreign objects)</li>
              <li><strong>Secondary Bronchi</strong>: Branch to each lung lobe (3 on right, 2 on left); also called lobar bronchi</li>
              <li><strong>Tertiary Bronchi</strong>: Branch to lung segments (10 on right, 8-10 on left); also called segmental bronchi</li>
              <li><strong>Bronchioles</strong>: Smaller airways (1mm or less); no cartilage, smooth muscle only; can constrict/dilate</li>
              <li><strong>Terminal Bronchioles</strong>: Smallest airways before alveoli; about 0.5mm diameter</li>
              <li><strong>Respiratory Bronchioles</strong>: Have some alveoli attached; transition zone</li>
            </ul>

            <h3>Alveolar Structure</h3>
            <ul>
              <li><strong>Alveolar Sacs</strong>: Clusters of alveoli; about 300-500 million alveoli total; surface area of 70-100 square meters (size of tennis court)</li>
              <li><strong>Alveolar Walls</strong>: Extremely thin (0.2-0.5 micrometers); single layer of cells (type I pneumocytes); allows rapid gas exchange</li>
              <li><strong>Type II Pneumocytes</strong>: Produce surfactant; reduces surface tension; prevents alveoli collapse</li>
              <li><strong>Alveolar Macrophages</strong>: Immune cells that remove dust and pathogens; "dust cells"</li>
              <li><strong>Pulmonary Capillaries</strong>: Dense network surrounding alveoli; 280 billion capillaries; total length about 1,500 miles</li>
            </ul>

            <h3>Respiratory Membrane</h3>
            <ul>
              <li><strong>Structure</strong>: Three layers - alveolar epithelium, basement membrane, capillary endothelium; total thickness 0.2-0.6 micrometers</li>
              <li><strong>Function</strong>: Allows rapid diffusion of oxygen and carbon dioxide; extremely thin for efficient gas exchange</li>
              <li><strong>Surface Area</strong>: Massive surface area maximizes gas exchange efficiency</li>
            </ul>

            <h3>Pleura and Pleural Cavity</h3>
            <ul>
              <li><strong>Visceral Pleura</strong>: Covers lung surface; thin, slippery membrane</li>
              <li><strong>Parietal Pleura</strong>: Lines chest wall and diaphragm; continuous with visceral pleura</li>
              <li><strong>Pleural Cavity</strong>: Space between pleurae; contains pleural fluid (10-20ml); creates negative pressure</li>
              <li><strong>Pleural Fluid</strong>: Reduces friction during breathing; creates surface tension to keep lungs expanded</li>
            </ul>

            <h3>Diaphragm and Respiratory Muscles</h3>
            <ul>
              <li><strong>Diaphragm</strong>: Dome-shaped muscle separating chest and abdomen; primary muscle of inspiration; contracts downward to increase chest volume</li>
              <li><strong>External Intercostals</strong>: Between ribs; elevate ribs during inspiration; increase chest width</li>
              <li><strong>Internal Intercostals</strong>: Between ribs; depress ribs during forced expiration</li>
              <li><strong>Accessory Muscles</strong>: Sternocleidomastoid, scalenes (inspiration); abdominal muscles (forced expiration)</li>
            </ul>

            <h2>Detailed Physiology</h2>
            
            <h3>Ventilation Mechanics</h3>
            <ul>
              <li><strong>Boyle's Law</strong>: Pressure and volume are inversely related; when volume increases, pressure decreases</li>
              <li><strong>Inspiration</strong>: 
                <ul>
                  <li>Diaphragm contracts (flattens)</li>
                  <li>Rib muscles contract (ribs rise)</li>
                  <li>Thoracic volume increases</li>
                  <li>Intrapulmonary pressure drops below atmospheric (758 mmHg vs 760 mmHg)</li>
                  <li>Air flows in due to pressure gradient</li>
                </ul>
              </li>
              <li><strong>Expiration</strong>:
                <ul>
                  <li>Diaphragm relaxes (dome shape returns)</li>
                  <li>Rib muscles relax (ribs lower)</li>
                  <li>Thoracic volume decreases</li>
                  <li>Intrapulmonary pressure rises above atmospheric (763 mmHg vs 760 mmHg)</li>
                  <li>Air flows out due to pressure gradient</li>
                </ul>
              </li>
            </ul>

            <h3>Gas Exchange (Diffusion)</h3>
            <ul>
              <li><strong>Partial Pressure</strong>: Pressure exerted by individual gas in mixture; oxygen partial pressure (PO2) in alveoli ~104 mmHg, in blood ~40 mmHg</li>
              <li><strong>Oxygen Diffusion</strong>: Moves from high concentration (alveoli) to low concentration (blood); driven by partial pressure gradient</li>
              <li><strong>Carbon Dioxide Diffusion</strong>: Moves from high concentration (blood ~46 mmHg) to low concentration (alveoli ~40 mmHg)</li>
              <li><strong>Fick's Law</strong>: Rate of diffusion depends on surface area, partial pressure difference, and membrane thickness</li>
              <li><strong>Factors Affecting Diffusion</strong>:
                <ul>
                  <li>Surface area: Larger = faster diffusion</li>
                  <li>Membrane thickness: Thinner = faster diffusion</li>
                  <li>Partial pressure gradient: Larger = faster diffusion</li>
                  <li>Gas solubility: CO2 is 20x more soluble than O2</li>
                </ul>
              </li>
            </ul>

            <h3>Oxygen Transport in Blood</h3>
            <ul>
              <li><strong>Hemoglobin Binding</strong>: 98.5% of oxygen bound to hemoglobin; each hemoglobin molecule can carry 4 oxygen molecules</li>
              <li><strong>Oxyhemoglobin</strong>: Hemoglobin with oxygen attached; bright red color</li>
              <li><strong>Deoxyhemoglobin</strong>: Hemoglobin without oxygen; darker red/blue color</li>
              <li><strong>Oxygen-Hemoglobin Dissociation Curve</strong>: S-shaped curve showing relationship between PO2 and hemoglobin saturation; at PO2 100 mmHg, 98% saturated</li>
              <li><strong>Factors Shifting Curve</strong>: Temperature, pH, CO2, 2,3-DPG; shift right = easier oxygen release to tissues</li>
              <li><strong>Dissolved Oxygen</strong>: 1.5% dissolved in plasma; not significant for transport but important for tissue PO2</li>
            </ul>

            <h3>Carbon Dioxide Transport</h3>
            <ul>
              <li><strong>Dissolved CO2</strong>: 7-10% transported as dissolved gas in plasma</li>
              <li><strong>Carbamino Compounds</strong>: 20-30% bound to hemoglobin and plasma proteins; forms carbaminohemoglobin</li>
              <li><strong>Bicarbonate Ions</strong>: 60-70% converted to bicarbonate (HCO3-) in red blood cells; catalyzed by carbonic anhydrase</li>
              <li><strong>Chloride Shift</strong>: Bicarbonate leaves red blood cell, chloride enters; maintains electrical balance</li>
              <li><strong>Reverse Process in Lungs</strong>: Bicarbonate converted back to CO2; CO2 diffuses into alveoli and is exhaled</li>
            </ul>

            <h3>Respiratory Regulation</h3>
            <ul>
              <li><strong>Respiratory Centers</strong>:
                <ul>
                  <li>Medullary respiratory center: Dorsal respiratory group (inspiration), Ventral respiratory group (expiration)</li>
                  <li>Pons: Pneumotaxic center (limits inspiration), Apneustic center (prolongs inspiration)</li>
                </ul>
              </li>
              <li><strong>Chemoreceptors</strong>:
                <ul>
                  <li>Central chemoreceptors: In medulla; respond to CO2 and pH changes in cerebrospinal fluid</li>
                  <li>Peripheral chemoreceptors: In carotid and aortic bodies; respond to O2, CO2, and pH in blood</li>
                </ul>
              </li>
              <li><strong>Mechanoreceptors</strong>:
                <ul>
                  <li>Stretch receptors: In lungs; prevent overinflation (Hering-Breuer reflex)</li>
                  <li>Irritant receptors: In airways; trigger cough and bronchoconstriction</li>
                </ul>
              </li>
            </ul>

            <h3>Lung Volumes and Capacities</h3>
            <ul>
              <li><strong>Tidal Volume (TV)</strong>: Normal breath in/out (~500ml)</li>
              <li><strong>Inspiratory Reserve Volume (IRV)</strong>: Extra air you can inhale after normal breath (~3,100ml)</li>
              <li><strong>Expiratory Reserve Volume (ERV)</strong>: Extra air you can exhale after normal breath (~1,200ml)</li>
              <li><strong>Residual Volume (RV)</strong>: Air remaining after maximum exhalation (~1,200ml); prevents lung collapse</li>
              <li><strong>Vital Capacity (VC)</strong>: Maximum air you can exhale after maximum inhalation (~4,800ml); TV + IRV + ERV</li>
              <li><strong>Total Lung Capacity (TLC)</strong>: Total air lungs can hold (~6,000ml); VC + RV</li>
            </ul>

            <h2>Why It's Critical</h2>
            <p>Without your respiratory system, your body couldn't get the oxygen it needs to survive. Every cell in your body depends on oxygen to function properly!</p>
