    <section class="py-16">
      <div class="container-custom">
        <div class="max-w-4xl mx-auto">
          <div class="prose prose-lg max-w-none">
            <h2>Your Digestive System - Food Processing Plant</h2>
            <p>Your digestive system is like a sophisticated food processing plant that takes in food, breaks it down into tiny pieces, extracts the nutrients your body needs, and gets rid of the waste.</p>
            
            <h2>Main Parts of Your Digestive System</h2>
            
            <h3>Mouth</h3>
            <ul>
              <li><strong>Teeth</strong>: Break food into smaller pieces</li>
              <li><strong>Saliva</strong>: Moistens food and starts chemical breakdown</li>
              <li><strong>Tongue</strong>: Mixes food with saliva and pushes it down</li>
              <li><strong>Taste buds</strong>: Help you enjoy food</li>
            </ul>

            <h3>Esophagus</h3>
            <ul>
              <li>Tube that carries food from mouth to stomach</li>
              <li>Uses muscle contractions (peristalsis) to move food</li>
              <li>About 10 inches long</li>
              <li>Works even if you're upside down!</li>
            </ul>

            <h3>Stomach</h3>
            <ul>
              <li>Muscular sac that stores and churns food</li>
              <li>Produces gastric juices (acid and enzymes)</li>
              <li>Breaks down proteins</li>
              <li>Can hold about 1 liter of food</li>
            </ul>

            <h3>Small Intestine</h3>
            <ul>
              <li>Long tube (about 20 feet) where most digestion happens</li>
              <li>Three parts: duodenum, jejunum, ileum</li>
              <li>Absorbs most nutrients into bloodstream</li>
              <li>Lined with tiny finger-like projections (villi)</li>
            </ul>

            <h3>Large Intestine (Colon)</h3>
            <ul>
              <li>Shorter but wider than small intestine</li>
              <li>Absorbs water and electrolytes</li>
              <li>Forms solid waste (stool)</li>
              <li>Contains beneficial bacteria</li>
            </ul>

            <h3>Rectum and Anus</h3>
            <ul>
              <li><strong>Rectum</strong>: Stores stool until elimination</li>
              <li><strong>Anus</strong>: Opening where waste exits body</li>
              <li>Has sphincter muscles for control</li>
            </ul>

            <h2>Accessory Organs</h2>
            
            <h3>Liver</h3>
            <ul>
              <li>Produces bile to help digest fats</li>
              <li>Processes nutrients from small intestine</li>
              <li>Filters toxins from blood</li>
              <li>Stores vitamins and minerals</li>
            </ul>

            <h3>Gallbladder</h3>
            <ul>
              <li>Stores bile produced by liver</li>
              <li>Releases bile when needed for fat digestion</li>
              <li>Small pear-shaped organ</li>
            </ul>

            <h3>Pancreas</h3>
            <ul>
              <li>Produces digestive enzymes</li>
              <li>Releases enzymes into small intestine</li>
              <li>Also produces insulin for blood sugar control</li>
            </ul>

            <h2>How Digestion Works</h2>
            
            <h3>Step 1: Ingestion</h3>
            <ol>
              <li>Food enters your mouth</li>
              <li>Teeth break it into smaller pieces</li>
              <li>Saliva moistens and begins chemical breakdown</li>
              <li>Tongue forms food into a ball (bolus)</li>
            </ol>

            <h3>Step 2: Swallowing</h3>
            <ol>
              <li>Bolus moves down esophagus</li>
              <li>Peristalsis pushes food toward stomach</li>
              <li>Muscle contractions work automatically</li>
            </ol>

            <h3>Step 3: Stomach Processing</h3>
            <ol>
              <li>Food enters stomach</li>
              <li>Stomach muscles churn and mix food</li>
              <li>Gastric juices break down proteins</li>
              <li>Food becomes liquid (chyme)</li>
              <li>Chyme slowly released to small intestine</li>
            </ol>

            <h3>Step 4: Small Intestine Digestion</h3>
            <ol>
              <li>Chyme enters small intestine</li>
              <li>Pancreatic enzymes break down carbohydrates, proteins, fats</li>
              <li>Bile from liver/gallbladder helps digest fats</li>
              <li>Nutrients absorbed through villi into bloodstream</li>
            </ol>

            <h3>Step 5: Large Intestine Processing</h3>
            <ol>
              <li>Remaining material enters large intestine</li>
              <li>Water and electrolytes absorbed</li>
              <li>Beneficial bacteria break down remaining material</li>
              <li>Waste material forms into stool</li>
            </ol>

            <h3>Step 6: Elimination</h3>
            <ol>
              <li>Stool moves to rectum</li>
              <li>Rectum signals need to eliminate</li>
              <li>Sphincter muscles relax</li>
              <li>Waste exits through anus</li>
            </ol>

            <h2>What Happens to Different Nutrients</h2>
            
            <h3>Carbohydrates</h3>
            <ul>
              <li>Broken down into simple sugars (glucose)</li>
              <li>Absorbed in small intestine</li>
              <li>Used for energy</li>
            </ul>

            <h3>Proteins</h3>
            <ul>
              <li>Broken down into amino acids</li>
              <li>Absorbed in small intestine</li>
              <li>Used for building and repairing tissues</li>
            </ul>

            <h3>Fats</h3>
            <ul>
              <li>Broken down into fatty acids and glycerol</li>
              <li>Absorbed in small intestine</li>
              <li>Used for energy and cell membranes</li>
            </ul>

            <h3>Vitamins and Minerals</h3>
            <ul>
              <li>Absorbed in small intestine</li>
              <li>Used for various body functions</li>
              <li>Some stored in liver</li>
            </ul>

            <h2>Digestive Enzymes</h2>
            <p>Special proteins that break down food:</p>
            
            <ul>
              <li><strong>Amylase</strong>: Breaks down carbohydrates</li>
              <li><strong>Protease</strong>: Breaks down proteins</li>
              <li><strong>Lipase</strong>: Breaks down fats</li>
              <li><strong>Produced by</strong>: Salivary glands, stomach, pancreas</li>
            </ul>

            <h2>Beneficial Bacteria</h2>
            <p>Your large intestine contains trillions of beneficial bacteria:</p>
            
            <ul>
              <li>Help break down remaining food</li>
              <li>Produce vitamins (K and some B vitamins)</li>
              <li>Protect against harmful bacteria</li>
              <li>Support immune system</li>
            </ul>

            <h2>Digestion Time</h2>
            <p>How long food takes to move through your system:</p>
            
            <ul>
              <li><strong>Mouth to stomach</strong>: 5-10 seconds</li>
              <li><strong>Stomach</strong>: 2-4 hours</li>
              <li><strong>Small intestine</strong>: 2-4 hours</li>
              <li><strong>Large intestine</strong>: 12-48 hours</li>
              <li><strong>Total time</strong>: 24-72 hours</li>
            </ul>

            <h2>Detailed Anatomy</h2>
            
            <h3>Gastrointestinal Wall Structure</h3>
            <p>All GI tract organs have four layers (from inside to outside):</p>
            <ul>
              <li><strong>Mucosa</strong>: Inner layer; contains epithelium (absorbs nutrients), lamina propria (connective tissue with blood vessels), muscularis mucosae (thin muscle layer)</li>
              <li><strong>Submucosa</strong>: Connective tissue layer; contains blood vessels, lymphatics, nerves (submucosal plexus)</li>
              <li><strong>Muscularis Externa</strong>: Two muscle layers - inner circular (constricts tube), outer longitudinal (shortens tube); contains myenteric plexus</li>
              <li><strong>Serosa/Adventitia</strong>: Outer layer; serosa in abdominal cavity (visceral peritoneum), adventitia where attached to other structures</li>
            </ul>

            <h3>Small Intestine Structure</h3>
            <ul>
              <li><strong>Length</strong>: About 20 feet (6 meters) in adults; longest part of GI tract</li>
              <li><strong>Duodenum</strong>: First 10 inches; receives bile and pancreatic juices; C-shaped curve around pancreas</li>
              <li><strong>Jejunum</strong>: Middle section (~8 feet); most nutrient absorption occurs here; thicker walls, more blood vessels</li>
              <li><strong>Ileum</strong>: Final section (~12 feet); connects to large intestine at ileocecal valve; absorbs remaining nutrients</li>
              <li><strong>Circular Folds (Plicae Circulares)</strong>: Permanent folds in intestinal wall; increase surface area 3x</li>
              <li><strong>Villi</strong>: Finger-like projections (1mm tall); increase surface area 10x; contain blood capillaries and lacteals (lymph vessels)</li>
              <li><strong>Microvilli</strong>: Tiny projections on villi cells; form "brush border"; increase surface area 20x; total increase: 600x</li>
            </ul>

            <h3>Stomach Anatomy</h3>
            <ul>
              <li><strong>Regions</strong>: Cardia (entrance), Fundus (top), Body (main part), Pylorus (exit to duodenum)</li>
              <li><strong>Gastric Pits</strong>: Openings in stomach lining; lead to gastric glands</li>
              <li><strong>Gastric Glands</strong>: Produce gastric juice; contain:
                <ul>
                  <li>Parietal cells: Produce hydrochloric acid (HCl) and intrinsic factor</li>
                  <li>Chief cells: Produce pepsinogen (precursor to pepsin)</li>
                  <li>Mucous cells: Produce protective mucus</li>
                </ul>
              </li>
              <li><strong>Rugae</strong>: Folds in empty stomach; allow expansion when full</li>
              <li><strong>Pyloric Sphincter</strong>: Muscle ring controlling exit from stomach to duodenum</li>
            </ul>

            <h3>Large Intestine Structure</h3>
            <ul>
              <li><strong>Length</strong>: About 5 feet (1.5 meters); wider but shorter than small intestine</li>
              <li><strong>Cecum</strong>: First part; pouch-like; appendix attached here</li>
              <li><strong>Colon</strong>: Four parts - ascending, transverse, descending, sigmoid</li>
              <li><strong>Haustra</strong>: Pouches in colon wall; allow expansion</li>
              <li><strong>Teniae Coli</strong>: Three bands of longitudinal muscle; create haustra</li>
              <li><strong>Epiploic Appendages</strong>: Fat-filled pouches on colon surface</li>
              <li><strong>Rectum</strong>: Final 6 inches; stores stool</li>
              <li><strong>Anal Canal</strong>: Last 1-2 inches; internal and external sphincters control elimination</li>
            </ul>

            <h3>Accessory Organs Anatomy</h3>
            <ul>
              <li><strong>Liver</strong>: Largest internal organ (~3 pounds); four lobes (right, left, caudate, quadrate); receives blood from hepatic artery and portal vein</li>
              <li><strong>Hepatic Lobules</strong>: Functional units of liver; hexagonal structures with central vein; process blood from portal vein</li>
              <li><strong>Gallbladder</strong>: Pear-shaped sac (~3-4 inches); stores and concentrates bile; releases bile via cystic duct</li>
              <li><strong>Pancreas</strong>: Fish-shaped organ; head, body, tail; exocrine (digestive enzymes) and endocrine (insulin, glucagon) functions</li>
              <li><strong>Pancreatic Ducts</strong>: Main duct joins common bile duct; empties into duodenum at ampulla of Vater</li>
            </ul>

            <h2>Detailed Physiology</h2>
            
            <h3>Digestive Enzymes and Their Actions</h3>
            <ul>
              <li><strong>Salivary Amylase</strong>: Mouth; breaks down starch into maltose; optimal pH 6.7-7.0; inactivated by stomach acid</li>
              <li><strong>Pepsin</strong>: Stomach; breaks down proteins into peptides; activated from pepsinogen by HCl; optimal pH 1.5-2.5</li>
              <li><strong>Pancreatic Amylase</strong>: Small intestine; continues starch digestion; optimal pH 7.0</li>
              <li><strong>Trypsin & Chymotrypsin</strong>: Small intestine; break down proteins into peptides; activated from precursors in duodenum</li>
              <li><strong>Lipase</strong>: Small intestine; breaks down fats into fatty acids and glycerol; requires bile for fat emulsification</li>
              <li><strong>Brush Border Enzymes</strong>: On microvilli; complete digestion:
                <ul>
                  <li>Maltase: Maltose ‚Üí glucose</li>
                  <li>Sucrase: Sucrose ‚Üí glucose + fructose</li>
                  <li>Lactase: Lactose ‚Üí glucose + galactose</li>
                  <li>Peptidases: Peptides ‚Üí amino acids</li>
                </ul>
              </li>
            </ul>

            <h3>Absorption Mechanisms</h3>
            <ul>
              <li><strong>Simple Diffusion</strong>: Small molecules (water, some vitamins) move down concentration gradient</li>
              <li><strong>Facilitated Diffusion</strong>: Molecules (fructose) use carrier proteins; no energy required</li>
              <li><strong>Active Transport</strong>: Molecules (glucose, amino acids) use carrier proteins and ATP; against concentration gradient</li>
              <li><strong>Endocytosis</strong>: Large molecules engulfed by cell membrane</li>
              <li><strong>Fat Absorption</strong>: 
                <ul>
                  <li>Fats emulsified by bile salts</li>
                  <li>Lipase breaks down fats</li>
                  <li>Fatty acids and monoglycerides form micelles</li>
                  <li>Absorbed into intestinal cells</li>
                  <li>Reformed into triglycerides</li>
                  <li>Packaged into chylomicrons</li>
                  <li>Enter lacteals (lymph vessels), not blood capillaries</li>
                </ul>
              </li>
            </ul>

            <h3>Gastric Secretion and Regulation</h3>
            <ul>
              <li><strong>Cephalic Phase</strong>: Before food enters; triggered by sight/smell/taste; vagus nerve stimulates gastric secretion</li>
              <li><strong>Gastric Phase</strong>: Food in stomach; distension and chemicals stimulate gastrin release; gastrin increases HCl and pepsinogen</li>
              <li><strong>Intestinal Phase</strong>: Chyme enters duodenum; secretin and cholecystokinin (CCK) released; slows gastric emptying</li>
              <li><strong>HCl Functions</strong>: Activates pepsinogen, denatures proteins, kills bacteria, breaks down connective tissue</li>
              <li><strong>Mucus Protection</strong>: Bicarbonate-rich mucus protects stomach wall from acid and enzymes</li>
            </ul>

            <h3>Hormonal Control of Digestion</h3>
            <ul>
              <li><strong>Gastrin</strong>: Stomach; stimulates HCl and pepsinogen secretion; increases gastric motility</li>
              <li><strong>Secretin</strong>: Duodenum; stimulated by acid chyme; increases pancreatic bicarbonate; decreases gastric acid</li>
              <li><strong>Cholecystokinin (CCK)</strong>: Duodenum; stimulated by fat/protein; contracts gallbladder; stimulates pancreatic enzyme secretion</li>
              <li><strong>Gastric Inhibitory Peptide (GIP)</strong>: Duodenum; decreases gastric motility and acid secretion</li>
              <li><strong>Motilin</strong>: Small intestine; increases intestinal motility between meals</li>
            </ul>

            <h3>Peristalsis and Motility</h3>
            <ul>
              <li><strong>Peristalsis</strong>: Wave-like muscle contractions; propels food forward; occurs throughout GI tract</li>
              <li><strong>Segmentation</strong>: Small intestine; mixing contractions; churns chyme; increases contact with absorptive surface</li>
              <li><strong>Mass Movements</strong>: Large intestine; strong peristaltic waves; move stool toward rectum; occur 1-3 times per day</li>
              <li><strong>Migrating Motor Complex</strong>: Between meals; sweeps remaining food and bacteria toward colon</li>
            </ul>

            <h3>Bile Production and Function</h3>
            <ul>
              <li><strong>Bile Components</strong>: Water, bile salts, cholesterol, bilirubin, electrolytes</li>
              <li><strong>Bile Salts</strong>: Emulsify fats; break large fat globules into smaller droplets; increase surface area for lipase</li>
              <li><strong>Enterohepatic Circulation</strong>: Bile salts reabsorbed in ileum; returned to liver; recycled 2-3 times per meal</li>
              <li><strong>Bilirubin</strong>: Waste product from red blood cell breakdown; gives bile and stool brown color</li>
            </ul>

            <h2>Why It's Critical</h2>
            <p>Without your digestive system, your body couldn't extract the nutrients it needs from food, leading to malnutrition and death. Every cell in your body depends on the nutrients provided by your digestive system!</p>
